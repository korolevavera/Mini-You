def get_practice_progress(user_id, practice_id):
    conn = get_db_connection()
    with conn.cursor(cursor_factory=RealDictCursor) as cur:
        cur.execute(
            "SELECT * FROM practice_progress WHERE user_id = %s AND practice_id = %s",
            (user_id, practice_id)
        )
        row = cur.fetchone()
    conn.close()
    if row:
        return {'completed_count': row['completed_count'], 'streak': row['streak'], 'last_used': row['last_used']}
    return {'completed_count': 0, 'streak': 0, 'last_used': None}

def update_practice_progress(user_id, practice_id, increment=True):
    conn = get_db_connection()
    with conn.cursor() as cur:
        cur.execute(
            "SELECT * FROM practice_progress WHERE user_id = %s AND practice_id = %s",
            (user_id, practice_id)
        )
        row = cur.fetchone()
        today = date.today().isoformat()
        if row:
            last_used = row[3] or ''
            if last_used.startswith(today) and increment:
                cur.execute(
                    "UPDATE practice_progress SET completed_count = completed_count + 1 WHERE user_id = %s AND practice_id = %s",
                    (user_id, practice_id)
                )
            elif last_used.startswith(today) and not increment:
                cur.execute(
                    "UPDATE practice_progress SET completed_count = completed_count - 1 WHERE user_id = %s AND practice_id = %s",
                    (user_id, practice_id)
                )
            else:
                if increment:
                    cur.execute(
                        "UPDATE practice_progress SET completed_count = completed_count + 1, last_used = %s, streak = streak + 1 WHERE user_id = %s AND practice_id = %s",
                        (datetime.now().isoformat(), user_id, practice_id)
                    )
                else:
                    cur.execute(
                        "UPDATE practice_progress SET completed_count = completed_count - 1, last_used = NULL, streak = streak - 1 WHERE user_id = %s AND practice_id = %s",
                        (user_id, practice_id)
                    )
        else:
            if increment:
                cur.execute(
                    "INSERT INTO practice_progress (user_id, practice_id, completed_count, streak, last_used) VALUES (%s, %s, 1, 1, %s)",
                    (user_id, practice_id, datetime.now().isoformat())
                )
        conn.commit()
    conn.close()

